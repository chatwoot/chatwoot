#!/usr/bin/env bash
# bootstrap-vps-ghcr.sh
# ---------------------
# Lean VPS deploy for Chatwoot Pro using a pre-built GHCR image.
# No local Docker build — just pull image + run migrations + start.
#
# Prereqs:
#   1. GitHub Actions workflow .github/workflows/build-and-push.yml has run
#      successfully at least once → image exists at
#      ghcr.io/fabriciomuaca1989/chatwoot-pro:latest
#   2. The GHCR package has been made PUBLIC (or you've docker-logged in to GHCR).
#   3. Domain DNS A-record already points at this VPS.
#
# Usage:
#   sudo ./bootstrap-vps-ghcr.sh chatwoot.exemplo.com admin@exemplo.com
#
# Resumable: re-runs skip steps already completed (.env stays the same).

set -euo pipefail

BLUE='\033[1;34m'; GREEN='\033[1;32m'; YELLOW='\033[1;33m'; RED='\033[1;31m'; NC='\033[0m'
log()  { echo -e "${BLUE}[bootstrap-ghcr]${NC} $*"; }
ok()   { echo -e "${GREEN}[ ok ]${NC} $*"; }
warn() { echo -e "${YELLOW}[warn]${NC} $*"; }
die()  { echo -e "${RED}[fail]${NC} $*"; exit 1; }

DOMAIN="${1:-}"
ADMIN_EMAIL="${2:-}"

if [[ -z "$DOMAIN" || -z "$ADMIN_EMAIL" ]]; then
  cat <<EOF
Usage: $0 <domain> <admin_email>

Examples:
  $0 chat.exemplo.com.br admin@exemplo.com.br
  $0 atendimento.minhaempresa.com.br fabricio@minhaempresa.com.br

DNS A-record for <domain> must already point to this VPS.
EOF
  exit 1
fi

if [[ ! -d engines/chatwoot_kanban ]]; then
  die "Run from inside the cloned fork (must contain engines/chatwoot_kanban/)"
fi

if [[ "$EUID" -ne 0 ]] && ! groups | grep -q docker; then
  die "Run as root OR add yourself to docker group: sudo usermod -aG docker $USER && newgrp docker"
fi

# -----------------------------------------------------------------------------
log "[1/9] Checking Docker..."
if ! command -v docker >/dev/null 2>&1; then
  log "Installing Docker..."
  curl -fsSL https://get.docker.com | sh
  systemctl enable --now docker
  ok "Docker installed."
else
  ok "Docker: $(docker --version)"
fi
docker compose version >/dev/null 2>&1 || \
  die "Docker Compose v2 missing. apt-get install docker-compose-plugin"

# -----------------------------------------------------------------------------
log "[2/9] Configuring .env..."
if [[ -f .env ]]; then
  warn ".env already exists — keeping current secrets. Delete it to regenerate."
else
  gen() { openssl rand -hex 32; }
  SECRET_KEY_BASE=$(openssl rand -hex 64)
  POSTGRES_PASSWORD=$(gen)
  REDIS_PASSWORD=$(gen)
  AR_PRIMARY=$(openssl rand -hex 32)
  AR_DETERMINISTIC=$(openssl rand -hex 32)
  AR_SALT=$(openssl rand -hex 32)

  cat > .env <<ENV_EOF
# Auto-generated by bootstrap-vps-ghcr.sh — $(date -u +%FT%TZ)
SECRET_KEY_BASE=$SECRET_KEY_BASE
FRONTEND_URL=https://$DOMAIN
FORCE_SSL=true
ENABLE_ACCOUNT_SIGNUP=false

POSTGRES_HOST=postgres
POSTGRES_PORT=5432
POSTGRES_DATABASE=chatwoot
POSTGRES_USERNAME=postgres
POSTGRES_PASSWORD=$POSTGRES_PASSWORD
RAILS_MAX_THREADS=5

REDIS_URL=redis://:$REDIS_PASSWORD@redis:6379
REDIS_PASSWORD=$REDIS_PASSWORD

ACTIVE_RECORD_ENCRYPTION_PRIMARY_KEY=$AR_PRIMARY
ACTIVE_RECORD_ENCRYPTION_DETERMINISTIC_KEY=$AR_DETERMINISTIC
ACTIVE_RECORD_ENCRYPTION_KEY_DERIVATION_SALT=$AR_SALT

MAILER_SENDER_EMAIL=Chatwoot <noreply@$DOMAIN>
SMTP_DOMAIN=$DOMAIN
ACTIVE_STORAGE_SERVICE=local
LOG_LEVEL=info
INSTALLATION_NAME=Chatwoot Pro
DEFAULT_LOCALE=pt_BR
ENV_EOF
  chmod 600 .env
  ok ".env created with strong random secrets."
fi

# -----------------------------------------------------------------------------
log "[3/9] Configuring Caddyfile..."
if [[ -f Caddyfile ]] && grep -q "$DOMAIN" Caddyfile; then
  ok "Caddyfile already targets $DOMAIN."
else
  cat > Caddyfile <<CADDY_EOF
$DOMAIN {
    encode gzip
    reverse_proxy rails:3000 {
        header_up Host {host}
        header_up X-Real-IP {remote_host}
        header_up X-Forwarded-For {remote_host}
        header_up X-Forwarded-Proto {scheme}
    }
    request_body {
        max_size 40MB
    }
}
CADDY_EOF
  ok "Caddyfile written."
fi

# -----------------------------------------------------------------------------
log "[4/9] Installing GHCR docker-compose.yaml..."
if [[ ! -f deploy/docker-compose.ghcr.yaml ]]; then
  die "deploy/docker-compose.ghcr.yaml missing — git pull or check the fork."
fi
if [[ -f docker-compose.yaml ]] && grep -q 'ghcr.io/fabriciomuaca1989/chatwoot-pro' docker-compose.yaml; then
  ok "Already using GHCR compose."
else
  if [[ -f docker-compose.yaml ]]; then
    cp docker-compose.yaml docker-compose.yaml.previous
    warn "Previous docker-compose.yaml backed up to docker-compose.yaml.previous"
  fi
  cp deploy/docker-compose.ghcr.yaml docker-compose.yaml
  ok "GHCR compose installed."
fi

# -----------------------------------------------------------------------------
log "[5/9] Pulling pre-built image from GHCR..."
docker compose pull rails sidekiq postgres redis caddy
ok "Images pulled."

# -----------------------------------------------------------------------------
log "[6/9] Firewall (best-effort)..."
if command -v ufw >/dev/null 2>&1; then
  ufw allow 22/tcp  >/dev/null 2>&1 || true
  ufw allow 80/tcp  >/dev/null 2>&1 || true
  ufw allow 443/tcp >/dev/null 2>&1 || true
  ok "ufw rules added."
else
  warn "ufw not installed. Open ports 80/443 in provider firewall."
fi

# -----------------------------------------------------------------------------
log "[7/9] Starting postgres + redis..."
docker compose up -d postgres redis
for i in $(seq 1 60); do
  if docker compose exec -T postgres pg_isready -U postgres >/dev/null 2>&1; then
    ok "Postgres ready."
    break
  fi
  [[ $i -eq 60 ]] && die "Postgres failed to become ready after 60s"
  sleep 1
done

# -----------------------------------------------------------------------------
log "[8/9] Running migrations (Chatwoot core + Kanban engine)..."
docker compose run --rm rails bundle exec rails db:chatwoot_prepare
ok "Migrations applied."

# -----------------------------------------------------------------------------
log "[9/9] Starting rails, sidekiq, caddy..."
docker compose up -d rails sidekiq caddy
sleep 8

# Admin user
ADMIN_PASSWORD=$(openssl rand -base64 18 | tr -d '/+=' | head -c 20)Aa1!
ADMIN_CREATED=$(docker compose exec -T rails bundle exec rails runner "
begin
  account = Account.first || Account.create!(name: 'Minha Empresa')
  user = User.find_by(email: '$ADMIN_EMAIL')
  if user.nil?
    user = User.create!(name: 'Admin', email: '$ADMIN_EMAIL', password: '$ADMIN_PASSWORD', display_name: 'Admin')
    AccountUser.create!(account: account, user: user, role: :administrator)
    puts 'CREATED'
  else
    puts 'EXISTS'
  end
rescue => e
  puts \"ERROR: #{e.message}\"
end
" 2>&1 | tail -1)

if [[ "$ADMIN_CREATED" == "CREATED" ]]; then
  ok "Admin user created."
elif [[ "$ADMIN_CREATED" == "EXISTS" ]]; then
  warn "User $ADMIN_EMAIL already exists — password unchanged."
  ADMIN_PASSWORD="(unchanged)"
else
  warn "Admin auto-create issue: $ADMIN_CREATED"
  ADMIN_PASSWORD="(create manually with: docker compose exec rails bundle exec rails c)"
fi

# Default kanban board
docker compose exec -T rails bundle exec rails runner "
account = Account.first
existing = ChatwootKanban::Board.find_by(account_id: account.id, name: 'Inbox principal')
unless existing
  b = ChatwootKanban::Board.create!(account_id: account.id, name: 'Inbox principal',
    description: 'Cards criados automaticamente de novas conversas',
    settings: { 'auto_create_from_conversations' => true })
  puts \"Board #{b.id} created\"
end
" 2>&1 | tail -1 || true

cat <<DONE

${GREEN}========================================================================${NC}
${GREEN}  Chatwoot Pro UP at  https://$DOMAIN${NC}
${GREEN}========================================================================${NC}

  Admin email:    $ADMIN_EMAIL
  Admin password: $ADMIN_PASSWORD
  Kanban board:   https://$DOMAIN/app/accounts/1/kanban

Update later (after a new GHCR build finishes):
  docker compose pull && docker compose up -d

Logs:
  docker compose logs -f rails sidekiq

${YELLOW}SAVE THE ADMIN PASSWORD NOW.${NC}

DONE
