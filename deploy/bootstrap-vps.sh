#!/usr/bin/env bash
# bootstrap-vps.sh
# ----------------
# One-shot installer for Chatwoot Pro (Chatwoot + Kanban engine).
#
# Run this INSIDE the extracted release directory on your Ubuntu/Debian VPS.
#
# Usage:
#   sudo ./bootstrap-vps.sh chatwoot.exemplo.com  admin@exemplo.com
#
# What it does:
#   1. Validates inputs (domain + admin email)
#   2. Installs Docker + Docker Compose if missing
#   3. Generates strong random secrets (SECRET_KEY_BASE, DB password, Redis password)
#   4. Generates Rails Active Record encryption keys (needed by future GLPI engine)
#   5. Writes a fully-configured .env file
#   6. Configures Caddyfile for your domain (HTTPS auto via Let's Encrypt)
#   7. Builds the Docker image from source (with engines included)
#   8. Initializes Postgres, runs ALL migrations (Chatwoot + Kanban engine)
#   9. Starts the stack (rails + sidekiq + postgres + redis + caddy)
#  10. Creates the first administrator account (with random password printed at the end)
#  11. Optionally seeds a default Kanban board with auto-create-from-conversations enabled
#
# Resumable: safe to re-run. Steps already completed are skipped.

set -euo pipefail

# -----------------------------------------------------------------------------
# Pretty output
# -----------------------------------------------------------------------------
BLUE='\033[1;34m'; GREEN='\033[1;32m'; YELLOW='\033[1;33m'; RED='\033[1;31m'; NC='\033[0m'
log()  { echo -e "${BLUE}[bootstrap]${NC} $*"; }
ok()   { echo -e "${GREEN}[ ok ]${NC} $*"; }
warn() { echo -e "${YELLOW}[warn]${NC} $*"; }
die()  { echo -e "${RED}[fail]${NC} $*"; exit 1; }

# -----------------------------------------------------------------------------
# Arguments
# -----------------------------------------------------------------------------
DOMAIN="${1:-}"
ADMIN_EMAIL="${2:-}"

if [[ -z "$DOMAIN" || -z "$ADMIN_EMAIL" ]]; then
  cat <<EOF
Usage: $0 <domain> <admin_email>

Examples:
  $0 chatwoot.exemplo.com admin@exemplo.com
  $0 atendimento.minhaempresa.com.br fabricio@minhaempresa.com.br

The domain must already point to this VPS via DNS (A record).
EOF
  exit 1
fi

# Validate we're inside the release directory
if [[ ! -f Gemfile || ! -d engines/chatwoot_kanban ]]; then
  die "Run this script from inside the extracted release directory (must contain Gemfile and engines/chatwoot_kanban/)"
fi

# Validate root or docker group membership
if [[ "$EUID" -ne 0 ]]; then
  if ! groups | grep -q docker; then
    die "Run as root OR as a user in the docker group (got: $(whoami))"
  fi
fi

# -----------------------------------------------------------------------------
# 1. Install Docker if missing
# -----------------------------------------------------------------------------
log "[1/11] Checking Docker..."
if ! command -v docker >/dev/null 2>&1; then
  log "Installing Docker..."
  curl -fsSL https://get.docker.com | sh
  systemctl enable --now docker
  ok "Docker installed."
else
  ok "Docker already installed: $(docker --version)"
fi

if ! docker compose version >/dev/null 2>&1; then
  die "Docker Compose v2 not found. Install docker-compose-plugin: apt-get install docker-compose-plugin"
fi

# -----------------------------------------------------------------------------
# 2. Generate secrets (only if .env does not exist yet)
# -----------------------------------------------------------------------------
log "[2/11] Configuring .env..."
if [[ -f .env ]]; then
  warn ".env already exists — keeping current secrets. Delete it to regenerate."
else
  gen() { openssl rand -hex 32; }
  SECRET_KEY_BASE=$(openssl rand -hex 64)
  POSTGRES_PASSWORD=$(gen)
  REDIS_PASSWORD=$(gen)

  # Rails 7 Active Record encryption keys (needed by GLPI engine, harmless otherwise)
  AR_PRIMARY=$(openssl rand -hex 32)
  AR_DETERMINISTIC=$(openssl rand -hex 32)
  AR_SALT=$(openssl rand -hex 32)

  cat > .env <<ENV_EOF
# Auto-generated by bootstrap-vps.sh — $(date -u +%FT%TZ)
SECRET_KEY_BASE=$SECRET_KEY_BASE
FRONTEND_URL=https://$DOMAIN
FORCE_SSL=true
ENABLE_ACCOUNT_SIGNUP=false

POSTGRES_HOST=postgres
POSTGRES_PORT=5432
POSTGRES_DATABASE=chatwoot
POSTGRES_USERNAME=postgres
POSTGRES_PASSWORD=$POSTGRES_PASSWORD
RAILS_MAX_THREADS=5

REDIS_URL=redis://:$REDIS_PASSWORD@redis:6379
REDIS_PASSWORD=$REDIS_PASSWORD

ACTIVE_RECORD_ENCRYPTION_PRIMARY_KEY=$AR_PRIMARY
ACTIVE_RECORD_ENCRYPTION_DETERMINISTIC_KEY=$AR_DETERMINISTIC
ACTIVE_RECORD_ENCRYPTION_KEY_DERIVATION_SALT=$AR_SALT

MAILER_SENDER_EMAIL=Chatwoot <noreply@$DOMAIN>
SMTP_DOMAIN=$DOMAIN
ACTIVE_STORAGE_SERVICE=local
LOG_LEVEL=info
INSTALLATION_NAME=Chatwoot Pro
DEFAULT_LOCALE=pt_BR
ENV_EOF
  chmod 600 .env
  ok ".env created with strong random secrets."
fi

# -----------------------------------------------------------------------------
# 3. Configure Caddyfile
# -----------------------------------------------------------------------------
log "[3/11] Configuring Caddyfile..."
if [[ -f Caddyfile ]] && grep -q "$DOMAIN" Caddyfile; then
  ok "Caddyfile already targets $DOMAIN."
else
  cat > Caddyfile <<CADDY_EOF
$DOMAIN {
    encode gzip
    reverse_proxy rails:3000 {
        header_up Host {host}
        header_up X-Real-IP {remote_host}
        header_up X-Forwarded-For {remote_host}
        header_up X-Forwarded-Proto {scheme}
    }
    request_body {
        max_size 40MB
    }
}
CADDY_EOF
  ok "Caddyfile written."
fi

# -----------------------------------------------------------------------------
# 4. Ensure docker-compose.yaml is our engine-aware version
# -----------------------------------------------------------------------------
# IMPORTANT: Chatwoot upstream ships its OWN docker-compose.yaml that uses
# separate rails.Dockerfile / vite.Dockerfile / sidekiq Dockerfile and references
# a chatwoot:development base image that doesn't exist on Docker Hub. We must
# REPLACE it with our single-stage build that bakes in engines/chatwoot_kanban.
log "[4/11] Ensuring docker-compose.yaml (engine-aware, single-image build)..."
if [[ ! -f deploy/docker-compose.yml ]]; then
  die "deploy/docker-compose.yml not found in repo — release tarball/clone is incomplete."
fi

# Detect upstream's version and overwrite if so. Our version contains
# 'chatwoot-pro:latest' as the image tag; upstream's references rails.Dockerfile.
if [[ -f docker-compose.yaml ]] && grep -q 'chatwoot-pro:latest' docker-compose.yaml; then
  ok "Engine-aware docker-compose.yaml already in place."
else
  if [[ -f docker-compose.yaml ]]; then
    cp docker-compose.yaml docker-compose.yaml.upstream-backup
    warn "Detected upstream docker-compose.yaml — backed up to docker-compose.yaml.upstream-backup"
  fi
  cp deploy/docker-compose.yml docker-compose.yaml
  ok "Installed engine-aware docker-compose.yaml from deploy/docker-compose.yml."
fi

# -----------------------------------------------------------------------------
# 5. Open firewall (best-effort)
# -----------------------------------------------------------------------------
log "[5/11] Configuring firewall (best-effort)..."
if command -v ufw >/dev/null 2>&1; then
  ufw allow 22/tcp  >/dev/null 2>&1 || true
  ufw allow 80/tcp  >/dev/null 2>&1 || true
  ufw allow 443/tcp >/dev/null 2>&1 || true
  ok "ufw rules added (if ufw is active)."
else
  warn "ufw not installed — skipping. Make sure ports 80/443 are open in your provider's firewall."
fi

# -----------------------------------------------------------------------------
# 6. Build Docker image
# -----------------------------------------------------------------------------
log "[6/11] Building Docker image (this takes 10-25 minutes the first time)..."
docker compose build
ok "Build complete."

# -----------------------------------------------------------------------------
# 7. Start postgres and redis first
# -----------------------------------------------------------------------------
log "[7/11] Starting postgres + redis..."
docker compose up -d postgres redis
sleep 5
# Wait for postgres to be healthy
for i in $(seq 1 60); do
  if docker compose exec -T postgres pg_isready -U postgres >/dev/null 2>&1; then
    ok "Postgres ready."
    break
  fi
  [[ $i -eq 60 ]] && die "Postgres failed to become ready after 60s"
  sleep 1
done

# -----------------------------------------------------------------------------
# 8. Initialize DB + run migrations
# -----------------------------------------------------------------------------
log "[8/11] Running database setup (chatwoot core + Kanban engine migrations)..."
docker compose run --rm rails bundle exec rails db:chatwoot_prepare
ok "All migrations applied."

# -----------------------------------------------------------------------------
# 9. Start the full stack
# -----------------------------------------------------------------------------
log "[9/11] Starting rails, sidekiq, caddy..."
docker compose up -d rails sidekiq caddy
sleep 8
ok "Stack is up."

# -----------------------------------------------------------------------------
# 10. Create first admin user
# -----------------------------------------------------------------------------
log "[10/11] Creating administrator account..."
ADMIN_PASSWORD=$(openssl rand -base64 18 | tr -d '/+=' | head -c 20)Aa1!

ADMIN_CREATED=$(docker compose exec -T rails bundle exec rails runner "
begin
  account = Account.first || Account.create!(name: 'Minha Empresa')
  user = User.find_by(email: '$ADMIN_EMAIL')
  if user.nil?
    user = User.create!(
      name: 'Admin', email: '$ADMIN_EMAIL',
      password: '$ADMIN_PASSWORD', display_name: 'Admin'
    )
    AccountUser.create!(account: account, user: user, role: :administrator)
    puts 'CREATED'
  else
    puts 'EXISTS'
  end
rescue => e
  puts \"ERROR: #{e.message}\"
end
" 2>&1 | tail -1)

if [[ "$ADMIN_CREATED" == "CREATED" ]]; then
  ok "Admin user created."
elif [[ "$ADMIN_CREATED" == "EXISTS" ]]; then
  warn "User with email $ADMIN_EMAIL already exists — password unchanged."
  ADMIN_PASSWORD="(unchanged — use your existing password or reset via Forgot Password)"
else
  warn "Could not auto-create admin: $ADMIN_CREATED"
  warn "Create one manually with: docker compose exec rails bundle exec rails c"
  ADMIN_PASSWORD="(see manual instructions above)"
fi

# -----------------------------------------------------------------------------
# 11. Seed default Kanban board with auto-create enabled
# -----------------------------------------------------------------------------
log "[11/11] Seeding default Kanban board..."
docker compose exec -T rails bundle exec rails runner "
account = Account.first
existing = ChatwootKanban::Board.find_by(account_id: account.id, name: 'Inbox principal')
unless existing
  b = ChatwootKanban::Board.create!(
    account_id: account.id,
    name: 'Inbox principal',
    description: 'Cards criados automaticamente de novas conversas',
    settings: { 'auto_create_from_conversations' => true }
  )
  puts \"Board #{b.id} created with columns: #{b.columns.pluck(:name).join(', ')}\"
end
" 2>&1 | tail -2

# -----------------------------------------------------------------------------
# Final report
# -----------------------------------------------------------------------------
cat <<DONE

${GREEN}========================================================================${NC}
${GREEN}  Chatwoot Pro is UP at  https://$DOMAIN${NC}
${GREEN}========================================================================${NC}

  Admin email:    $ADMIN_EMAIL
  Admin password: $ADMIN_PASSWORD

  Kanban board:   https://$DOMAIN/app/accounts/1/kanban

Useful commands:

  # Watch logs
  docker compose logs -f rails sidekiq

  # Rails console
  docker compose exec rails bundle exec rails c

  # Stop / start / restart
  docker compose down
  docker compose up -d
  docker compose restart rails sidekiq

  # Run engine specs (sanity)
  docker compose exec rails bundle exec rspec engines/chatwoot_kanban/spec

If HTTPS fails on first request, Caddy may still be fetching the cert.
Wait 60 seconds and refresh. Check Caddy logs:

  docker compose logs caddy | tail -50

${YELLOW}STORE THE ADMIN PASSWORD NOW — it will not be shown again.${NC}

DONE
