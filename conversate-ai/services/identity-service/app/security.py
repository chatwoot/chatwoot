# This will be a function that gets the token from the header, decodes it,
# and returns the current user. For now, we'll just define the core logic.

def decode_access_token(token: str):
    try:
        payload = jwt.decode(token, SECRET_KEY, algorithms=[ALGORITHM])
        email: str = payload.get("sub")
        if email is None:
            return None
        return email
    except JWTError:
        return None
